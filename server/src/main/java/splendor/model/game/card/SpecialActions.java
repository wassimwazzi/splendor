package splendor.model.game.card;

/**
 * Special actions that can be obtained from buying red cards.
 */
public enum SpecialActions {
  /**
   * taking a free level 2 card.
   */
  TakeFreeLevel2,
  /**
   * taking a free level 1 card.
   */
  TakeFreeLevel1,
  /**
   * cloning a card.
   */
  Clone,
  /**
   * choosing a noble to unlock.
   */
  ChooseNoble,
  /**
   * reserving a noble.
   */
  ReserveNoble,
  /**
   * get double the bonus.
   */
  DoubleBonus
  // might need to add more.
}
